#include "libft.h"

int		ft_str_is_void(const char *str)
{
	int i;

	i = 0;
	if (!str || !str[0])
		return (1);
	while (str[i] && ft_isspace(str[i]))
		i++;
	if (!str[i])
		return (1);
	return (0);
}
